        The institute was granted financial and academic autonomy from June 21, 2004. As a result, the autonomous VJTI has implemented the revised syllabus for the students in June 2004 at the undergraduate and postgraduate levels by implementing much valued credit system. The revised scheme of examinations, which follows a grading system and letter grade point system has been implemented. An important part of curriculum is the practically oriented project at the final year of graduation and the dissertations offered by the postgraduate students, as also the in-plant training undergone by the Third Year Diploma students.<br><br>

