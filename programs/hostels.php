  There are four messes run by students. Hostel fees are based on three seaters, two seaters or single seater including electricity charges.
   The hostel students amenities fund and medical charges are payable at the begining of each term.
The hostels are provided with dining halls and the residents organize themselves into clubs and run the messes 
under the control of Joint Managing Committee of the students Messes (JMC). 
There are four messes at present and each mess has an elected General Secretary, Joint Secretary and an Auditor.
Hostel accommodation is available for students enrolled for each full time course. 
All attempts are made to provide hostel accommodation to as many students as possible. 
However, it is not possible to provide hostel accommodations to all the applicants. 
The students waiting for hostel accommodations in the first semester will have to make their own arrangement 
to stay in the city up to the time they are finally admitted to the hostel. 
Applications for hostel accommodations should be made on the prescribed form available from the office of the hostel after 
admission to the institute is confirmed.<br>

