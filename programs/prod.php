 <h3>Production Departments</h3>
  <p style="position:absolute;text-align:left;left:25px;font-size:17px;font-family:"Calibri"">
  The department of Production engineering was established in 1959 as the department of industrial engineering 
  to conduct part time course leading to postgraduate diploma in production engineering. 
  At present the department offers undergraduate and postgraduate programmes as well as postgraduate 
  diploma in industrial engineering (DIE) which is a multi disciplinary programme.
  The B.E. production programme is of sandwich type and is recognized by the board of apprenticeship training. 
  Because of this, the students undertake live projects in various industries as a part of their curriculum. 
  Postgraduate students also undertake industrial projects.<br> 
  The department has very good industrial interaction through these projects and industrial visiting faculty.
  The department has three major laboratories - production process laboratory, meteorology laboratory and 
  computer laboratory. Production process laboratory is equipped with sophisticated machine tools for 
  students training and also for consultation work such as prototype making and testing. 
  Meterology laboratory is equipped with various precision measuring instruments and standards. 
  Various projects have been carried out in calibration and standardization activities in the laboratory. 
  This department  also undertakes Testing/Consultancy in Trailer Design, etc.

