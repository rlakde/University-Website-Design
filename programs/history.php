 The Bachelor Degree courses in Textile, Mechanical Engineering and Electrical Engineering were started In 1946,1947 and 1949 respectively The Institute is affiliated to the University of Bombay and was one of the fourteen institutes selected by the Central Government for further development with grant-in-aid from the Centre.<br><br>

Prior to 1960 Victoria Jubilee Technical Institute was the only Institute offering Engineering degree and Post Graduate courses under the University of Bombay and enjoyed defacto autonomy. Victoria Jubilee Technical Institute has played a very vital role in curriculum development such as the introduction of new courses, framing their syllabi and their examination schemes at the University level.<br><br>

In 1998, the Institute was renamed from its old name Victoria Jubilee Technical Institute to its present name Veermata Jijabai Technological Institute.<br><br>
