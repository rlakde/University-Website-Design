 <h1> TECHNOVANZA
</h1>
 <p style="position:absolute;text-align:left;left:25px; font-size:17px;width:930px">
 Technovanza is the Annual Technomanagement Event organized by VJTI.
  The Objective of Technovanza is to educate people about the principles of Engineering and thus spreading the knowledge of Engineering and Technology to the society at large.
  Since 2001 when the first event was held, Technovanza has grown from strength to strength and it is now a highly anticipated event in the Institute’s calendar.
  The event’s popularity has grown in leaps and bounds over the years and last year close to 20000 people from all over the country attended the event,
  leading to its grand success. Technovanza is the platform for students to apply the theoretical knowledge they gather in class-rooms and use it practically by making exhibits that can explain engineering principles to lay people.
  It is a great opportunity for students to showcase their potential and helps them in two ways.
  They make the exhibits with their own hands and then explain the concepts to the people who visit their stalls.
  The confidence the students get in first developing a concept, developing the prototype and then demonstrating it in front of people, goes a long way in making them ready to face the real world.
  The three day event has a various activities which are of interest to all type of visitors, young and old who visit or participate in the event.
  The Conference provides a great platform for people to keep abreast of the latest developments.
  There are many lectures by eminent speakers and group discussions in which many experts take part.
  For students and youngsters who visit there are a lot of competitions in which they can take part and win prizes.
  Parents and elders can visit the Exhibition and understand how various gadgets which they use in every-day life work.
  To the under-privileged students in schools the social initiative Pratigya brings technology to their door-step and inspires them to become engineers tomorrow.
  The power of Technovanza is seen by the number of eminent personalities who take part each year.
  Over the years a lot of companies have shown keen interest in associating with the event and help us by sponsorships, which enable us continuously to improve the Event.
  And as the events grow in quality and size it becomes a news event and results in tremendous media coverage which adds to its popularity.
  It is our endeavor to make the 2013 edition a bigger and better one with high quality projects and a great seminar.
  It is my dream that Technovanza will be established in the years to come as a national event befitting the status of VJTI.</p>
