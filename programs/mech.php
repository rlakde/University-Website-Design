 <h3>Mechanical Department</h3>
  <p style="position:absolute;text-align:left;left:25px;font-size:17px;font-family:"Calibri"">
  Established in 1887, the department of Mechanical engineering remains among the premier departments of VJTI. 
  It has had a long and illustrious history and offers courses at the graduate and postgraduate levels. 
  The department is recognized for doctoral research by the University of Mumbai.
  The department has kept pace with the rapid development of computer aided methods of design and 
  manufacture with state of the art robotics and CAD/CAM facilities.
  The laboratories in the department are recognized in a number of areas as testing facilities. 
  The department also has workshops for training in smithy, carpentry, welding and machine tools.
