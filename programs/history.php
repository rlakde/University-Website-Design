 <h3>&nbsp;&nbsp;A Brief History of VJTI</h3>
 <p style="position:absolute;text-align:left;left:25px;font-size:17px;font-family:"Calibri"">
 Established in 1887, the V. J. T. Institute started with only two departments, namely the Sir J. J. School of Mechanical Engineering and the Ripon Textile School for the two branches of industry in which Bombay was vitally interested at the time. The aim was to with a desire to meet the technical manpower requirements of Bombay. The first step in the expansion of the Institute was taken in 1903 when courses in Electrical Engineering were introduced. The Technical & Applied Chemistry Department was added in 1906 which was followed in 1914 by the Department of Sanitary Engineering. In 1913 by G. R. No. 1850 of 27th June, the institute was recognised by the Government of Bombay as the Central Technological Institute, Bombay Province. In the early days, the Institute was housed in Byculla in a building donated for the purpose by Sir Dinshaw Maneckji Petit. In the course of development, the Institute's building at Byculla proved insufficient for the growing number of students who sought admission and soon after the First World War in 1923, the Institute moved to its present and more commodious, specially constructed building at Matunga, which represents the most important landmark in the history of the Institute. This would not have been possible but for the liberal grants given by the Government and the Bombay Millowners' Association. A new block of northern light roof type construction was added in 1931 for housing the textile machinery and equipment which was obtained through the kind offices of Sir Ness Wadia. <br>

