  The department of Textile Manufactures was started as the Ripon Textile School in the early days of the institute.  <br><br>

The department is currently located in the northern light steel trussed roof building. It has variety of modern spinning and weaving machines and laboratory equipment. The department carries out industry consultancy projects and is recognized by the Government of India as an official testing facility.  <br><br>

BTech and MText degrees are awarded at the graduate and postgraduate levels. In-plant training is an integral part of the learning process at the diploma and B. Text levels.<br><br>
